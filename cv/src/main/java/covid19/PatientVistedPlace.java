package covid19;

public class PatientVistedPlace {
	 private Address vistedaddress ;
	 private String visitPlaceName ;
	 private String  visitDate;
	
	public Address getVistedaddress() {
		return vistedaddress;
	}
	public void setVistedaddress(Address vistedaddress) {
		this.vistedaddress = vistedaddress;
	}
	public String getVisitPlaceName() {
		return visitPlaceName;
	}
	public void setVisitPlaceName(String visitPlaceName) {
		this.visitPlaceName = visitPlaceName;
	}
	public String getVisitDate() {
		return visitDate;
	}
	public void setVisitDate(String visitDate) {
		this.visitDate = visitDate;
	}
	
	 
}
